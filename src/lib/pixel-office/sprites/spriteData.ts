/**
 * Resolve a template row (array of palette keys) into hex color strings.
 * '_' maps to transparent (empty string).
 */
function resolveTemplateRow(row: string[], pal: CharPalette): string[] {
  return row.map((key) => {
    switch (key) {
      case '_': return '';
      case 'H': return pal.hair;
      case 'h': return pal.hairDark;
      case 'S': return pal.skin;
      case 's': return pal.skinShadow;
      case 'E': return pal.eye;
      case 'C': return pal.clothing;
      case 'c': return pal.clothingDark;
      case 'P': return pal.pants;
      case 'p': return pal.pantsDark;
      case 'B': return pal.shoe;
      case 'O': return pal.outline;
      default: return '';
    }
  });
}

function resolveTemplate(template: string[][], pal: CharPalette): SpriteData {
  return template.map((row) => resolveTemplateRow(row, pal));
}

